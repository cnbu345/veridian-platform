// Get compliance checklist for a state
export function getComplianceChecklist(stateCode: string) {
  const regulation = getStateRegulation(stateCode)
  
  const checklist = [
    'Register business entity with Secretary of State',
    'Obtain EIN from IRS',
    'Determine money transmitter license requirements',
  ]
  
  if (regulation.moneyTransmitter.includes('required')) {
    checklist.push('Apply for money transmitter license')
    checklist.push('Prepare financial statements and bonding requirements')
    checklist.push('Implement AML/KYC procedures')
  }
  
  if (stateCode === 'NY') {
    checklist.push('Apply for BitLicense')
    checklist.push('Designate compliance officer')
    checklist.push('Maintain transaction records for 7 years')
  }
  
  if (stateCode === 'CA') {
    checklist.push('Register with DFPI')
    checklist.push('Comply with California Consumer Privacy Act')
  }
  
  if (stateCode === 'TX' || stateCode === 'WY' || stateCode === 'FL') {
    checklist.push('Review state-specific tax exemptions')
  }
  
  return checklist
}